package Loops;

public class whileloop {
	
	    public static void main(String[] args) {
	        int i = 1;

	        // A while loop that prints numbers from 1 to 5
	        while (i <= 5) {
	            System.out.println("Number: " + i);
	            i++; // Increment the value of i
	        }
	    }
	}


